-- INICIO BLOQUE: CREACION TABLA RUTAS
-- CATALOGO DE TIPOS DE RUTA SEGUN CERCANIA AL HUB (LOCAL/REMOTO/...); DEFINE EL MEDIO DE ENVIO Y SI EXIGE GUIA EXTERNA.
-- LOS VALORES SE ADMINISTRAN COMO DATOS (SIN CHECK FIJO EN nombre) PARA SUMAR TIPOS NUEVOS SIN ALTERAR LA ESTRUCTURA.
-- SE DEFINE ANTES DE tarifas PORQUE ESTA LA REFERENCIA POR CLAVE FORANEA.
CREATE TABLE IF NOT EXISTS rutas (
    id INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    nombre VARCHAR(40) NOT NULL,
    medioenvio VARCHAR(80) NOT NULL,
    exigeguiaexterna TINYINT(1) NOT NULL DEFAULT 0,
    estado VARCHAR(20) NOT NULL DEFAULT 'ACTIVO',
    fecharegistro DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT chkrutasexigeguia
        CHECK (exigeguiaexterna IN (0, 1)),
    CONSTRAINT chkrutasestado
        CHECK (estado IN ('ACTIVO', 'INACTIVO')),
    CONSTRAINT unqrutanombre UNIQUE (nombre)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_spanish_ci;
-- FIN BLOQUE: CREACION TABLA RUTAS

-- INICIO BLOQUE: CREACION TABLA TARIFAS
-- DEFINE LA TARIFA DE ENVIO POR TIENDA Y TIPO DE RUTA (FK rutas) PARA CALCULAR montoapagar.
CREATE TABLE IF NOT EXISTS tarifas (
    id INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    idtienda INT UNSIGNED NOT NULL,
    idruta INT UNSIGNED NOT NULL,
    preciobase DECIMAL(12,2) NOT NULL,
    precioporpaquete DECIMAL(12,2) NOT NULL DEFAULT 0.00,
    fecharegistro DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT chktarifasprecios
        CHECK (preciobase >= 0 AND precioporpaquete >= 0),
    CONSTRAINT unqtarifatiendaruta UNIQUE (idtienda, idruta),
    CONSTRAINT fktarifastiendas
        FOREIGN KEY (idtienda) REFERENCES tiendas(id),
    CONSTRAINT fktarifasrutas
        FOREIGN KEY (idruta) REFERENCES rutas(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_spanish_ci;
-- FIN BLOQUE: CREACION TABLA TARIFAS

