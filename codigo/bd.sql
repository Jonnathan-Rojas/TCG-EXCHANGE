-- INICIO BLOQUE: CREACION TABLA ENVIOS
-- DEFINE LA CABECERA DEL PROCESO DE ENVIO ENTRE REMITENTE Y DESTINATARIO.
-- LA CLAVE PRIMARIA ES EL NUMERO DE RASTREO: SE ASIGNA EN APLICACION (NO AUTO_INCREMENT).
-- FORMATO OBLIGATORIO DEL ID: PREFIJO CRE + AAAAMMDDHHMMSS (14 DIGITOS), SIN GUIONES (17 CARACTERES).
CREATE TABLE IF NOT EXISTS envios (
    id VARCHAR(17) PRIMARY KEY,
    idremitente VARCHAR(20) NOT NULL,
    iddestinatario VARCHAR(20) NOT NULL,
    idtiendaorigen INT UNSIGNED NOT NULL,
    idtiendadestino INT UNSIGNED NOT NULL,
    -- ID TIENDA QUE CUMPLE ROL DE CENTRO DE DISTRIBUCION (NOMBRE TECNICO DE COLUMNA: idhub).
    idhub INT UNSIGNED NULL,
    -- GUARDA LA RUTA ELEGIDA DEL CATALOGO rutas (DATA-DRIVEN: LOCAL, REMOTO, EN TIENDA, ...).
    -- LONGITUD ALINEADA A rutas.nombre (VARCHAR(40)). LOS VALORES VALIDOS SE CONTROLAN EN APLICACION CONTRA rutas ACTIVAS (SIN CHECK FIJO).
    formaenvio VARCHAR(40) NOT NULL,
    montoapagar DECIMAL(12,2) NOT NULL,
    estado VARCHAR(80) NOT NULL,
    CONSTRAINT chkenviosidrastreo
        CHECK (id REGEXP '^CRE[0-9]{14}$'),
    CONSTRAINT chkenviosmontoapagar
        CHECK (montoapagar >= 0)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_spanish_ci;
-- FIN BLOQUE: CREACION TABLA ENVIOS

