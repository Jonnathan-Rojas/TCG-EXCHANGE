    CONSTRAINT chkusuariosperfil
        CHECK (perfil IN ('CLIENTE', 'TIENDA', 'ADMINISTRADOR')),
    CONSTRAINT chkusuariosestado
        CHECK (estado IN ('ACTIVO', 'BLOQUEADO', 'INACTIVO'))
